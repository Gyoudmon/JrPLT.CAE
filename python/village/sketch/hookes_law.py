# 场景：胡克定律

# 实验得出关系表达式：Δl = 2m

# 定义函数：
def Δl(m):
    return 2 * m

# 调用函数：
print(Δl(1))
print(Δl(2))

# 古灵精怪：
print(Δl('猫'))

# 发现问题，引出“类型”概念，
# 但 Python 无法直接解决此问题

